<p>The engine groaned to life as she turned the key, the low rumble breaking the oppressive quiet around her. She adjusted the rearview mirror, her pale face staring back at her for a fleeting moment before she shifted into gear. The tires crunched over the gravel as she backed out of the driveway, the sound fading as she turned onto the main road. The drive to the town square was a short one, but the weight of the sheriff’s call made each minute stretch into an eternity.</p>
